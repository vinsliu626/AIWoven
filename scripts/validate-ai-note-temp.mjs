import { chromium } from "playwright";
import nextEnv from "@next/env";

const env = nextEnv.loadEnvConfig(process.cwd(), true).combinedEnv;
const browser = await chromium.launch({ headless: true, executablePath: "C:/Program Files/Google/Chrome/Application/chrome.exe" });
const page = await browser.newPage({ viewport: { width: 1440, height: 1000 } });
const errors = [];
page.on("console", (message) => { if (message.type() === "error") errors.push(message.text()); });
await page.goto("http://localhost:3000/api/auth/signin", { waitUntil: "domcontentloaded" });
await page.getByLabel("Email").fill(env.E2E_USER_EMAIL);
await page.getByLabel("Password").fill(env.E2E_USER_PASSWORD);
await page.getByRole("button", { name: /sign in with (?:e2e test account|local test account)/i }).click({ timeout: 5_000 }).catch(() => {});
await page.waitForTimeout(1_500);
const sessionResponse = await page.request.get("http://localhost:3000/api/auth/session");
const session = await sessionResponse.json();
if (!session?.user?.email) throw new Error("Local browser sign-in did not establish a session.");
const appPage = await page.context().newPage();
appPage.on("console", (message) => { if (message.type() === "error") errors.push(message.text()); });
await appPage.goto("http://localhost:3000/ai-note", { waitUntil: "domcontentloaded", timeout: 30_000 });
await appPage.getByRole("heading", { name: "AI Note", exact: true }).last().waitFor({ timeout: 30_000 });
const desktopText = await appPage.locator("body").innerText();
await appPage.screenshot({ path: "C:/Users/gcswz/.codex/visualizations/2026/07/18/019f736a-6d1a-7521-9acd-c6254a36121e/ai-note-auth-desktop.png" });
await appPage.getByRole("button", { name: "Record", exact: true }).click();
const recordText = await appPage.locator("body").innerText();
await appPage.getByRole("button", { name: "Text", exact: true }).click();
await appPage.getByRole("textbox").fill("A short source paragraph for UI validation.");
const textModeText = await appPage.locator("body").innerText();
await appPage.setViewportSize({ width: 390, height: 844 });
const mobileOverflow = await appPage.evaluate(() => document.documentElement.scrollWidth - document.documentElement.clientWidth);
await appPage.screenshot({ path: "C:/Users/gcswz/.codex/visualizations/2026/07/18/019f736a-6d1a-7521-9acd-c6254a36121e/ai-note-auth-mobile.png" });
console.log(JSON.stringify({ desktop: { quotaPillsRemoved: !/Today|Text limit|Item cap/.test(desktopText) }, record: { internalDiagnosticsRemoved: !/\bChunks\b|\bSession\b|\bFinalize\b/.test(recordText), hasSimpleRecorder: recordText.includes("Start Recording") && recordText.includes("Start recording when you are ready.") }, text: { hasClear: textModeText.includes("Clear"), hasCount: textModeText.includes("43 chars") }, mobile: { horizontalOverflow: mobileOverflow }, consoleErrors: errors }, null, 2));
await browser.close();
